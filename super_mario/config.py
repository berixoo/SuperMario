# ═══════════════════════════════════════════════════════════════════
# 关卡文本地图字符映射
# 每个字符代表地图中一个 32x32 瓦片的内容
# ═══════════════════════════════════════════════════════════════════
TILE_GROUND = '#'           # 地面或砖块 (固体, 可站立)
