"""
关卡数据模块 (level_data.py)
=============================
职责: 存储 3 个关卡的文本地图数据, 使用字符映射定义每个瓦片的内容。

设计理念 — 为什么用文本地图?
  1. 直观可读 — 打开文件就能看懂关卡结构, 老师和同学不需要额外工具
  2. 易于修改 — 改一个字符就能调整关卡 (加个 # 就是平台, 加个 E 就是敌人)
  3. 便于版本管理 — Git diff 可以清晰显示每次关卡修改了哪些位置
  4. 适合课程展示 — 直接在代码中就能展示关卡设计思路

字符到实体的映射关系 (定义在 config.py):
  # = 地面/砖块 (固体)    P = 玩家出生点 (每关唯一)
  C = 金币 (可收集)        E = 蘑菇敌人 (左右巡逻, 受重力)
  B = 飞行敌人 (sin浮动)   Q = 问号砖块 (顶击随机奖励)
  U = 已触发问号砖块       F = 终点旗 (通关目标)
  . = 空白 (无实体)

关卡设计三原则:
  1. 地面完整性 — 最后一行全程可走, 不会掉出地图
  2. 旗帜可达性 — 旗帜下方有砖块, 右端有阶梯逐级可达 (每级间距 ≤ 2 格)
  3. 敌人有支撑 — 蘑菇敌人下方有平台 (会受重力下落后站稳)

模块间关系:
  被 game.py 的 start_level() 读取 → 传给 core.parse_level() 解析为实体列表
"""


